FIELD_TYPE_BIGINT      = 0x00000002
FIELD_TYPE_FLOAT       = 0x00000004
FIELD_TYPE_DOUBLE      = 0x00000008
FIELD_TYPE_STRING      = 0x00000010
FIELD_TYPE_BLOB        = 0x00000020
FIELD_TYPE_DATETIME    = 0x00000040
FIELD_TYPE_PSWD        = 0x00000080

